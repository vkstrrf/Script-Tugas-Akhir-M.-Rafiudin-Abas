R_THRESHOLD_R = 0.823   # ambang batas r sesuai Aldrian & Susanto (99% CI)

def pearson_map(clim_arr, clim_ref):
    """
    Hitung korelasi Pearson antara siklus tahunan setiap grid (12 nilai)
    dengan siklus tahunan titik referensi.
    Mengembalikan array 2D (nlat x nlon) berisi nilai r.
    """
    nlat_, nlon_ = clim_arr.shape[1], clim_arr.shape[2]
    r_map = np.full((nlat_, nlon_), np.nan)
    ref_anom = clim_ref - clim_ref.mean()
    denom_ref = np.sqrt((ref_anom**2).sum())

    for i in range(nlat_):
        for j in range(nlon_):
            grid_ts = clim_arr[:, i, j]
            if np.isnan(grid_ts).any():
                continue
            grid_anom = grid_ts - grid_ts.mean()
            denom_grid = np.sqrt((grid_anom**2).sum())
            if denom_grid == 0 or denom_ref == 0:
                continue
            r_map[i, j] = (grid_anom * ref_anom).sum() / (denom_grid * denom_ref)
    return r_map

