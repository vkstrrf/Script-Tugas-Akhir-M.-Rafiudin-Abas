months_np = rain["time.month"].values   # array integer bulan (1-12)

std_map = np.full((12, nlat, nlon), np.nan)
for m in range(1, 13):
    idx_m = np.where(months_np == m)[0]
    data_m = rain_np[idx_m]             # (nyears, nlat, nlon)
    clim_m = clim[m-1]                  # (nlat, nlon)
    std_map[m-1] = np.sqrt(((data_m - clim_m[np.newaxis])**2).sum(axis=0) / (len(idx_m) - 1))

