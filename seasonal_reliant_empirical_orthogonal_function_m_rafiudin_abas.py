def plot_korelasi_gabungan(dict_r_nino, dict_r_dmi, tau_vals, N_eff):
    """
    Menampilkan grafik garis korelasi lead-lag untuk Niño3.4 dan DMI dalam satu
    figure, dengan satu sub-panel per mode dan sumbu-x kontinu (musim x lag).
    """
    modes = list(dict_r_nino.keys())

    # Aproksimasi t kritis alpha=0.05 (two-tailed, valid untuk sampel menengah-besar)
    alpha = 0.05
    t_crit = 1.96
    r_crit = t_crit / np.sqrt(t_crit**2 + N_eff - 2)

    fig, axes = plt.subplots(len(modes), 1, figsize=(10, 4.5 * len(modes)), sharex=True)
    if len(modes) == 1:
        axes = [axes]

    n_tau = len(tau_vals)
    x_ticks = np.arange(n_tau * 4)
    x_labels = SEASONS * n_tau

    # Loop tiap mode: flatten korelasi musiman ke sumbu-x kontinu, lalu plot garis
    for idx, mode in enumerate(modes):
        ax = axes[idx]
        y_nino, y_dmi = [], []
        for t_idx in range(n_tau):
            for s in SEASONS:
                y_nino.append(dict_r_nino[mode][s][t_idx])
                y_dmi.append(dict_r_dmi[mode][s][t_idx])

        ax.plot(x_ticks, y_nino, marker='*', markersize=6, color='#d73027',
               linewidth=2, label='Niño3.4')
        ax.plot(x_ticks, y_dmi, marker='*', markersize=6, color='#4575b4',
               linewidth=2, label='DMI (IOD)')

        # Garis bantu horizontal & batas signifikansi
        for y_line in np.arange(-0.8, 0.9, 0.2):
            ax.axhline(y_line, color='grey', linestyle='--', linewidth=0.7, alpha=0.5)
        ax.axhline(r_crit,  color='#968b5a', linestyle='-.', linewidth=1.2, alpha=0.7)
        ax.axhline(-r_crit, color='#968b5a', linestyle='-.', linewidth=1.2, alpha=0.7)

        ax.set_ylim(-0.9, 0.9)
        ax.set_yticks(np.arange(-0.8, 0.9, 0.2))

        # Garis pemisah antar-lag & label Y(-1)/Y(0)/dst di bawah tiap blok musim
        for i in range(1, n_tau):
            ax.axvline(i * 4 - 0.5, color='k', linewidth=0.8)
        for i, tau in enumerate(tau_vals):
            ax.text(i * 4 + 1.5, -0.75, f'Y({tau:+d})', ha='center', va='center',
                   fontsize=12, fontweight='bold')

        panel_label = chr(ord('a') + idx)
        ax.set_title(f'({panel_label}) korelasi (mode {mode}, Niño3.4 & DMI)', fontsize=11)
        ax.tick_params(axis='x', which='both', bottom=True, top=False, direction='in')
        if idx == 0:
            ax.legend(loc='upper right', framealpha=0.9, fontsize=9)

    axes[-1].set_xticks(x_ticks)
    axes[-1].set_xticklabels(x_labels, fontsize=10)

    fig.suptitle(
        'Analisis Korelasi Lead-Lag Komponen Utama S-EOF\n'
        'Sumatera Bagian Selatan (1983–2024)',
        fontweight='bold', fontsize=14, y=0.96
    )
    plt.tight_layout(rect=[0, 0, 1, 0.93])
    plt.savefig('fig_06_leadlag_gabungan.png', dpi=300, bbox_inches='tight')
    plt.show()

