    """Helper: set batas peta dan dekorasi standar."""
    ax.set_extent([LON_MIN, LON_MAX, LAT_MIN, LAT_MAX], crs=ccrs.PlateCarree())
    ax.coastlines(resolution='10m', linewidth=0.7)
