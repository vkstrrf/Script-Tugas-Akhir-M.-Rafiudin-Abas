def muat_indeks(path, kolom_nilai, sep=','):
    """Muat indeks iklim bulanan dari CSV dan kembalikan xarray DataArray."""
    df = pd.read_csv(path, sep=sep)
    df['time'] = pd.to_datetime(df[['year', 'month']].assign(day=1))
    df = df.sort_values('time').reset_index(drop=True)
    da = xr.DataArray(
        df[kolom_nilai].values,
        dims=['time'],
        coords={'time': df['time'].values}
    )
    return da

def rata_musiman_indeks(da_monthly):
    """
    Rata-ratakan indeks bulanan ke musiman sesuai Persamaan 3.15.
    Mengembalikan dict {season: DataArray(year,)}
    """
    seasonal_idx = {}
    years = np.unique(da_monthly.time.dt.year.values)
    for season, months in SEASON_MONTHS.items():
        vals, yrs = [], []
        for y in years:
            if season == 'DJF':
                des = da_monthly.sel(time=(da_monthly.time.dt.year == y) &
                                        (da_monthly.time.dt.month == 12))
                jan = da_monthly.sel(time=(da_monthly.time.dt.year == y+1) &
                                        (da_monthly.time.dt.month == 1))
                feb = da_monthly.sel(time=(da_monthly.time.dt.year == y+1) &
                                        (da_monthly.time.dt.month == 2))
                if des.size == 0 or jan.size == 0 or feb.size == 0:
                    continue
                val = float(xr.concat([des, jan, feb], dim='time').mean())
            else:
                sel = da_monthly.sel(time=da_monthly.time.dt.month.isin(months) &
                                         (da_monthly.time.dt.year == y))
                if sel.size < 3:
                    continue
                val = float(sel.mean())
            vals.append(val)
            yrs.append(y)
        seasonal_idx[season] = xr.DataArray(vals, dims=['year'], coords={'year': yrs})
    return seasonal_idx

def korelasi_leadlag(pc_series, idx_seasonal, years_pc, tau_range=(-1, 1)):
    """
    Hitung korelasi lead-lag r_{k,s}(τ) sesuai Persamaan 3.16.

    Parameter
    ----------
    pc_series   : array (N,) — PC tahunan mode-k
    idx_seasonal: dict {season: array (N_year,)} — indeks iklim musiman
    years_pc    : array (N,) — tahun yang bersesuaian dengan pc_series
    tau_range   : (tau_min, tau_max) dalam satuan tahun

    Mengembalikan
    -------------
    r_matrix : dict {season: array (n_tau,)} — koefisien korelasi
    tau_vals : array (n_tau,) — nilai lag τ
    """
    tau_vals = np.arange(tau_range[0], tau_range[1] + 1)
    r_matrix = {s: np.full(len(tau_vals), np.nan) for s in SEASONS}
    pc_mean = pc_series.mean()

    for t_idx, tau in enumerate(tau_vals):
        for season in SEASONS:
            idx_da = idx_seasonal[season]
            idx_yrs = idx_da.year.values

            # Sesuaikan tahun: I_{s, y+tau}, lalu ambil irisan tahun yang valid
            years_shifted = years_pc + tau
            common = np.intersect1d(years_shifted, idx_yrs)
            if len(common) < 5:   # minimal 5 pasangan untuk korelasi bermakna
                continue

            mask_pc  = np.isin(years_shifted, common)
            mask_idx = np.isin(idx_yrs, common)
            pc_sub  = pc_series[mask_pc]
            idx_sub = idx_da.values[mask_idx]
            idx_mean = idx_sub.mean()

            num  = np.sum((pc_sub - pc_mean) * (idx_sub - idx_mean))
            den  = np.sqrt(np.sum((pc_sub - pc_mean)**2) * np.sum((idx_sub - idx_mean)**2))
            if den == 0:
                continue
            r_matrix[season][t_idx] = num / den

    return r_matrix, tau_vals


