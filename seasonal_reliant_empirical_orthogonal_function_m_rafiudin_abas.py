def hitung_rata_musiman(da, lat_dim=lat_name, lon_dim=lon_name):
    """
    Mengelompokkan anomali bulanan menjadi rata-rata musiman per tahun.

    Mengembalikan dict {season: DataArray(year, lat, lon)}
    dengan DJF merujuk pada Desember tahun-0 + Jan,Feb tahun-1,
    diindeks pada tahun-0 sebagai tahun ENSO.
    """
    seasonal = {}
    years = np.unique(da.time.dt.year.values)

    for season, months in SEASON_MONTHS.items():
        maps = []
        year_list = []
        for y in years:
            if season == 'DJF':
                # Des tahun-y, Jan+Feb tahun-y+1
                try:
                    des = da.sel(time=(da.time.dt.year == y)   & (da.time.dt.month == 12))
                    jan = da.sel(time=(da.time.dt.year == y+1) & (da.time.dt.month == 1))
                    feb = da.sel(time=(da.time.dt.year == y+1) & (da.time.dt.month == 2))
                    if des.time.size == 0 or jan.time.size == 0 or feb.time.size == 0:
                        continue
                    mean_3bln = xr.concat([des, jan, feb], dim='time').mean(dim='time')
                except Exception:
                    continue
            else:
                sel = da.sel(time=da.time.dt.month.isin(months) & (da.time.dt.year == y))
                if sel.time.size < 3:
                    continue
                mean_3bln = sel.mean(dim='time')

            maps.append(mean_3bln.values)
            year_list.append(y)

        arr = np.array(maps)  # (N_year, n_lat, n_lon)
        seasonal[season] = xr.DataArray(
            arr,
            dims=['year', lat_dim, lon_dim],
            coords={'year': year_list, lat_dim: da[lat_dim].values, lon_dim: da[lon_dim].values},
            attrs={'units': 'mm/bulan', 'long_name': f'Anomali rata-rata {season}'}
        )

    return seasonal

seasonal_anom = hitung_rata_musiman(anomali)

