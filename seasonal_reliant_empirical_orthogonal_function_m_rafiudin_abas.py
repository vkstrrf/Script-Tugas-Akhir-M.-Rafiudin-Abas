def plot_leadlag(r_matrices, tau_vals, mode_k, idx_name, alpha=0.05, N_eff=None):
    """
    Tampilkan diagram korelasi lead-lag 2D:
      sumbu-x = musim (JJA, SON, DJF, MAM)
      sumbu-y = lag τ (tahun)
    dengan hatching pada area yang melewati batas signifikansi two-tailed.
    """
    n_eff  = N_eff if N_eff else len(years_valid)
    t_crit = stats.t.ppf(1 - alpha / 2, df=n_eff - 2)
    r_crit = t_crit / np.sqrt(t_crit**2 + n_eff - 2)

    R_mat = np.array([r_matrices[s] for s in SEASONS]).T   # (n_tau, 4)

    fig, ax = plt.subplots(figsize=(7, 5))
    im = ax.pcolormesh(np.arange(4), tau_vals, R_mat, cmap='RdBu', vmin=-1, vmax=1,
                       shading='nearest')

    sig = np.abs(R_mat) >= r_crit
    for t_i, tau in enumerate(tau_vals):
        for s_i in range(4):
            if sig[t_i, s_i]:
                ax.add_patch(plt.Rectangle((s_i - 0.5, tau - 0.5), 1, 1,
                                           fill=False, hatch='...', edgecolor='k',
                                           linewidth=0, alpha=0.6))

    ax.set_xticks(np.arange(4)); ax.set_xticklabels(SEASONS)
    ax.set_yticks(tau_vals); ax.set_yticklabels([f'τ={t:+d}' for t in tau_vals])
    ax.set_xlabel('Musim Indeks Iklim')
    ax.set_ylabel('Lag τ (tahun)\n[ τ>0: indeks mendahului PC ]')
    ax.set_title(
        f'Korelasi Lead-Lag: PC Mode-{mode_k} vs {idx_name}\n'
        f'(titik = signifikan p<{alpha:.2f}, r_crit={r_crit:.2f})',
        fontweight='bold'
    )
    cb = plt.colorbar(im, ax=ax, shrink=0.85)
    cb.set_label('Koefisien Korelasi (r)', fontsize=10)
    plt.tight_layout()
    plt.savefig(f'fig_06_leadlag_mode{mode_k}_{idx_name}.png')
    plt.show()

nino34_monthly = muat_indeks(NINO34_PATH, kolom_nilai='nino34')
dmi_monthly    = muat_indeks(DMI_PATH,    kolom_nilai='dmi')
nino34_seas = rata_musiman_indeks(nino34_monthly)
dmi_seas    = rata_musiman_indeks(dmi_monthly)

